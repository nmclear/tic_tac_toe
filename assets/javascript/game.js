//DOCUMENT READY
$(document).ready(function() {

 //========================================================================================================================
//VARIABLES
//========================================================================================================================

var firstPlayer = true;
var secondPlayer = false;




//========================================================================================================================
//FUNCTIONS
//========================================================================================================================












//========================================================================================================================
// PLAY GAME
//========================================================================================================================


$('.gameBtn').click(function(){
    if(firstPlayer == true){
        $(this).text("X");
        $(this).prop('disabled', true);
        firstPlayer = false;
        secondPlayer =  true;
    }
    else {
        $(this).text("O");
        $(this).prop('disabled', true);
        firstPlayer = true;
        secondPlayer =  false;
    }


});





});